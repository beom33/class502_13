package Chapter3;

public class Ex01 {
    public static void main(String[] args) {
        int mathScore = 487;
        int engScore = 70;

        int totalScore = mathScore + engScore ; // 총점 구하기
        System.out.println(totalScore);

        double avgScore = totalScore / 2.0; // 평균 구하기, avgScore 는 평균 점수 의미
        System.out.println(avgScore);
    }
 // 총점 557 , 평균 278.5
}
