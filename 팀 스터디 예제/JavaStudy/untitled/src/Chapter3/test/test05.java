package Chapter3.test;

public class test05 {
    public static void main(String[] args) {
        int num1 = 2;
        int num2 = 10;

        System.out.println(num1 & num2);
        System.out.println(num1 | num2);
        System.out.println(num1 ^ num2);
        System.out.println(~num1);
    }
   /*
    2
    10
    8
   -3
   */
}
